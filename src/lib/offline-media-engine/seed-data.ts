import type { BookContent } from "./types";

function makeChunks(bookId: string, chapters: { title: string; text: string }[]): BookContent["chunks"] {
  const chunks: BookContent["chunks"] = [];
  let ci = 0;
  for (const ch of chapters) {
    const sentences = ch.text.match(/[^.!?؟،\n]{20,}/g) ?? [ch.text];
    const size = 3;
    for (let i = 0; i < sentences.length; i += size) {
      const text = sentences.slice(i, i + size).join(". ").trim();
      if (!text) continue;
      chunks.push({
        id:           `${bookId}_${ci}_${Math.floor(i / size)}`,
        bookId,
        chapterIndex: ci,
        chunkIndex:   Math.floor(i / size),
        text,
        tokens:       [],
        tfidf:        [],
      });
    }
    ci++;
  }
  return chunks;
}

